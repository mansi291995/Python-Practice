my_list=[1,2,3,4,('6',2,3),{'name':'mansi','age':25},5,6,7]
print(my_list[1:5:2])
#complete list
print(my_list[::])
#start and end posistion
print(my_list[1:3:])
#start end and steps
print(my_list[1:4:1])
#negative indexing with start
print(my_list[-1:-4:1])
#negative indexing with end
print(my_list[1:-3:1])
#negative indexing with steps
print(my_list[1:7:-2])
#all negative
print(my_list[-1:-5:-1])